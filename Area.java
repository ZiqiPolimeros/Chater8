

public class Area
{
//NO fields

   
//methods  
   public static double getArea(double r)
   {
      return Math.PI*r*r;
   }
   
   public static double getArea(double w,double l)
   {
      return  w*l;
   }
   
   public static double getArea(int h,int r)
   {
      return Math.PI*h*r*r;
   }

}
